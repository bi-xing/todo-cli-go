package main

import (
	"encoding/json"
	"fmt"
	"log"
	"os"

	"github.com/spf13/cobra"
)

const datafile = "todo.json"

func listTask() []string {
	tasks := []string{}
	data, err := os.ReadFile(datafile)
	if err == nil {
		json.Unmarshal(data, &tasks)
	}
	return tasks
}

func saveTask(tasks []string) {
	data, err := json.MarshalIndent(tasks, "", "\t")
	if err != nil {
		log.Fatalf("Error: %v", err)
	}
	os.WriteFile(datafile, data, 0644)
}

var listCmd = &cobra.Command{
	Use:   "list",
	Short: "List all tasks currently.",
	RunE: func(cmd *cobra.Command, args []string) error {
		tasks := listTask()
		if len(tasks) == 0 {
			fmt.Println("No tasks listed.")
			return nil
		}
		for i, task := range tasks {
			fmt.Printf("%d. %s\n", i+1, task)
		}
		return nil
	},
}

var addCmd = &cobra.Command{
	Use:   "save",
	Short: "Save current tasks and overwrite.",
	Args:  cobra.ExactArgs(1),
	RunE: func(cmd *cobra.Command, args []string) error {
		newTask := args[0]
		tasks := listTask()
		tasks = append(tasks, newTask)
		saveTask(tasks)
		fmt.Printf("Task added: %s\n", newTask)
		return nil
	},
}

var rootCmd = &cobra.Command{
	Use:   "todo",
	Short: "A todo-CLI in Go.",
	RunE: func(cmd *cobra.Command, args []string) error {
		cmd.Help()
		return nil
	},
}

func init() {
	rootCmd.AddCommand(listCmd)
	rootCmd.AddCommand(addCmd)
}

